#Программа, которая считет по просьбе пользователя
#Пользователь может указать начало и конец счета, а также шаг

start = int(input(" Введите начало отсчета: "))

finish = int(input("\n Введите конечную точку отсчета: "))

step = int(input("\n Укажите шаг для счета: "))

for i in range(start, finish, step):
    print(i, end= "")